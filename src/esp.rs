pub struct ESP {
    enabled: bool,
}

impl ESP {
    pub fn new() -> Self {
        ESP { enabled: false }
    }

    pub fn enable(&mut self) {
        self.enabled = true;
    }

    pub fn disable(&mut self) {
        self.enabled = false;
    }

    pub fn is_enabled(&self) -> bool {
        self.enabled
    }
}